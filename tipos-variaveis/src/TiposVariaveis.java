public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
           
              // Declarando e atribuindo valores a variáveis de tipos primitivos
              int idade = 30;
              double altura = 1.75;
              char genero = 'M';
              boolean casado = true;
              
              // Imprimindo os valores das variáveis na tela
              System.out.println("Idade: " + idade);
              System.out.println("Altura: " + altura);
              System.out.println("Gênero: " + genero);
              System.out.println("Casado: " + casado);
              
              // Realizando operações com variáveis de tipos primitivos
              double imc = 80 / (altura * altura); // Cálculo do índice de massa corporal
              boolean temMaisDe40Anos = idade > 40; // Verificando se a idade é maior que 40
              
              // Imprimindo os resultados das operações na tela
              System.out.println("IMC: " + imc);
              System.out.println("Tem mais de 40 anos? " + temMaisDe40Anos);
            }
    
          


    }

